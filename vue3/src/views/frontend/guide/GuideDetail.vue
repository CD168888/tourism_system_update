<script setup>
import { ref, onMounted, computed } from 'vue'
import { useRoute, useRouter } from 'vue-router'
import request from '@/utils/request'
import { formatDate } from '@/utils/dateUtils'
import { useUserStore } from '@/store/user'
import { ElMessage } from 'element-plus'
import { View, Calendar, Star, StarFilled, Share, User, ArrowRight } from '@element-plus/icons-vue'
import MarkdownIt from 'markdown-it'

const baseAPI = process.env.VUE_APP_BASE_API || '/api'
const route = useRoute()
const router = useRouter()
const userStore = useUserStore()
const guide = ref(null)
const isCollected = ref(false)
const loading = ref(true)
const relatedGuides = ref([])

// 初始化 Markdown 解析器
const md = new MarkdownIt({
  html: true,        // 允许HTML标签
  linkify: true,     // 自动转换URL为链接
  typographer: true, // 智能标点
  breaks: true       // 转换换行符为<br>
})

// 获取图片完整URL
const getImageUrl = (url) => {
  if (!url) return ''
  return url.startsWith('http') ? url : baseAPI + url
}

// 格式化数字
const formatNumber = (num) => {
  if (!num) return '0'
  if (num >= 10000) {
    return (num / 10000).toFixed(1) + 'w'
  } else if (num >= 1000) {
    return (num / 1000).toFixed(1) + 'k'
  }
  return num.toString()
}

// 处理富文本内容中的图片路径
const processedContent = computed(() => {
  if (!guide.value || !guide.value.content) return ''
  
  let content = guide.value.content
  
  // 判断是否为Markdown格式（包含 # 或 ## 等标题标记）
  const isMarkdown = /^#{1,6}\s+.+$/m.test(content) || 
                     /^\*\*.*\*\*/.test(content) ||
                     /^\*\s+.+$/m.test(content) ||
                     /^\d+\.\s+.+$/m.test(content)
  
  // 如果是Markdown，先转换为HTML
  if (isMarkdown) {
    content = md.render(content)
  }
  
  // 处理图片路径：将相对路径转换为完整URL
  content = content.replace(/<img([^>]*?)src="([^"]*?)"([^>]*?)>/gi, (match, before, src, after) => {
    // 如果是相对路径，添加baseAPI前缀
    if (src && !src.startsWith('http://') && !src.startsWith('https://') && !src.startsWith('data:')) {
      src = baseAPI + src
    }
    return `<img${before}src="${src}"${after}>`
  })
  
  // 处理视频路径（如果有）
  content = content.replace(/<video([^>]*?)src="([^"]*?)"([^>]*?)>/gi, (match, before, src, after) => {
    if (src && !src.startsWith('http://') && !src.startsWith('https://')) {
      src = baseAPI + src
    }
    return `<video${before}src="${src}"${after}>`
  })
  
  return content
})

const fetchGuide = async () => {
  loading.value = true
  const id = route.params.id
  try {
    await request.get(`/guide/${id}`, {}, {
      showDefaultMsg: false,
      onSuccess: (res) => {
        guide.value = res
        document.title = res.title + ' - 旅游攻略'
        
        // 如果用户已登录，查询是否已收藏
        if (userStore.isLoggedIn) {
          checkIsCollected(id)
        }
        
        // 获取相关攻略
        fetchRelatedGuides()
      }
    })
  } catch (error) {
    console.error('获取攻略详情失败:', error)
  } finally {
    loading.value = false
  }
}

// 获取相关攻略
const fetchRelatedGuides = async () => {
  if (!guide.value) return
  
  try {
    await request.get('/guide/related', { 
      guideId: guide.value.id,
      limit: 3
    }, {
      showDefaultMsg: false,
      onSuccess: (data) => {
        relatedGuides.value = data || []
      }
    })
  } catch (error) {
    console.error('获取相关攻略失败:', error)
  }
}

// 查询是否已收藏
const checkIsCollected = async (guideId) => {
  if (!userStore.isLoggedIn) return
  
  try {
    await request.get('/collection/isCollected', { guideId }, {
      showDefaultMsg: false,
      onSuccess: (data) => {
        isCollected.value = data
      }
    })
  } catch (error) {
    console.error('查询收藏状态出错:', error)
  }
}

// 处理收藏/取消收藏
const handleCollectionToggle = async () => {
  if (!userStore.isLoggedIn) {
    ElMessage.warning('请先登录')
    router.push('/login?redirect=' + route.fullPath)
    return
  }
  
  try {
    if (isCollected.value) {
      // 取消收藏
      await request.delete('/collection/cancel?guideId=' + guide.value.id, {
        successMsg: '取消收藏成功',
        onSuccess: () => {
          isCollected.value = false
        }
      })
    } else {
      // 添加收藏
      await request.post('/collection/add', { guideId: guide.value.id }, {
        successMsg: '收藏成功',
        onSuccess: () => {
          isCollected.value = true
        }
      })
    }
  } catch (error) {
    console.error('收藏操作失败:', error)
  }
}

// 分享功能
const handleShare = () => {
  // 复制链接到剪贴板
  const url = window.location.href
  navigator.clipboard.writeText(url).then(() => {
    ElMessage.success('链接已复制，快去分享吧！')
  }).catch(() => {
    ElMessage.error('复制失败，请手动复制地址栏链接')
  })
}

// 前往其他攻略
const goToGuide = (id) => {
  router.push(`/guide/detail/${id}`)
}

// 返回上一页
const goBack = () => {
  router.back()
}

onMounted(fetchGuide)
</script>

